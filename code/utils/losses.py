def mse_loss(input1, input2):
    return torch.mean((input1 - input2)**2)

class VATMseLoss(nn.Module):
    def __init__(self, xi=10.0, epi=6.0, ip=1):
        super(VATMseLoss, self).__init__()
        self.xi = xi
        self.epi = epi
        self.ip = ip
        self.loss = mse_loss

    def forward(self, model, proj_model, x):
        with torch.no_grad():
            pred= F.softmax(model(x), dim=1)
            enc = proj_model(pred)[1] # 2*lbsxcxhxw
            
        d = torch.rand(x.shape).sub(0.5).to(x.device)
        d = _l2_normalize(d) 
        with _disable_tracking_bn_stats(model):
            # calc adversarial direction
            for _ in range(self.ip):
                d.requires_grad_(True)
                pred_hat = model(x + self.xi * d)
                logp_hat = F.softmax(pred_hat, dim=1)
                enc_hat = proj_model(logp_hat)[1]
                
                adv_distance = self.loss(enc_hat, enc) 
                adv_distance.backward()
                d = _l2_normalize(d.grad)
                model.zero_grad()
                
            r_adv = d * self.epi
            pred_hat = model(x + r_adv)
            logp_hat = F.softmax(pred_hat, dim=1)
            enc_hat = proj_model(logp_hat)[1]
            latent_lds = self.loss(enc_hat, enc) 
        return latent_lds